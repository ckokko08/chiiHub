import UIKit

class ColorManager: UIColor {
    
    func darkblueColor() ->UIColor{
            return UIColor(red: 8/255, green: 33/255, blue: 97/255, alpha: 1)
    }
    
    func whitecolor() ->UIColor{
            return UIColor(red: 255/255, green: 255/255, blue: 255/255, alpha: 1)
    }
    
    func pinkcolor() ->UIColor{
        return UIColor(red: 253/255, green: 64/255, blue: 128/255, alpha: 1)
    }
    
    

}
